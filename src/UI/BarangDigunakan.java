    private void loadDataBarangDigunakan() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Barang Digunakan");
    model.addColumn("ID Event");
    model.addColumn("ID Barang");
    model.addColumn("Jumlah Keluar");
    model.addColumn("Tanggal Keluar");
    model.addColumn("Tanggal Masuk");
    model.addColumn("Kondisi Setelah Masuk");
    model.addColumn("Created At");
    model.addColumn("Updated At");

    try {
        Connection conn = DbConnection.getConnection();
        if (conn == null) {
            throw new SQLException("Gagal koneksi ke database.");
        }

        String sql = "SELECT * FROM barang_digunakan";
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_barang_digunakan"),
                rs.getInt("id_event"),
                rs.getInt("id_barang"),
                rs.getInt("jumlah_keluar"),
                rs.getString("tanggal_keluar"),
                rs.getString("tanggal_masuk"),
                rs.getString("kondisi_setelah_masuk"),
                rs.getString("created_at"),
                rs.getString("updated_at")
            });
        }

        jTable1.setModel(model); // ganti jika nama tabel kamu berbeda

    } catch (SQLException e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal memuat data barang digunakan: " + e.getMessage());
    }
}

    private void aturTampilanTabel() {
    // Atur header font
    jTable1.getTableHeader().setFont(new java.awt.Font("SansSerif", java.awt.Font.BOLD | java.awt.Font.ITALIC, 12));
    jTable1.getTableHeader().setBackground(java.awt.Color.WHITE);
    jTable1.getTableHeader().setForeground(java.awt.Color.BLACK);
    
    // Tengahkan teks header
    javax.swing.table.DefaultTableCellRenderer headerRenderer = 
    (javax.swing.table.DefaultTableCellRenderer) jTable1.getTableHeader().getDefaultRenderer();
    headerRenderer.setHorizontalAlignment(javax.swing.SwingConstants.CENTER);

    // Atur isi tabel
    jTable1.setFont(new java.awt.Font("SansSerif", java.awt.Font.PLAIN, 12));
    jTable1.setBackground(java.awt.Color.WHITE);
    jTable1.setForeground(java.awt.Color.BLACK);
    jTable1.setRowHeight(25);

    // Tengah hanya kolom 0â€“3 (ID dan Jumlah)
javax.swing.table.DefaultTableCellRenderer centerRenderer = new javax.swing.table.DefaultTableCellRenderer();
centerRenderer.setHorizontalAlignment(javax.swing.SwingConstants.CENTER);

jTable1.getColumnModel().getColumn(0).setCellRenderer(centerRenderer); // ID Barang Digunakan
jTable1.getColumnModel().getColumn(1).setCellRenderer(centerRenderer); // ID Event
jTable1.getColumnModel().getColumn(2).setCellRenderer(centerRenderer); // ID Barang
jTable1.getColumnModel().getColumn(3).setCellRenderer(centerRenderer); // Jumlah Keluar

    
    // Hilangkan belang striping
    jTable1.setDefaultRenderer(Object.class, new javax.swing.table.DefaultTableCellRenderer() {
        @Override
        public java.awt.Component getTableCellRendererComponent(javax.swing.JTable table, Object value,
                boolean isSelected, boolean hasFocus, int row, int column) {
            java.awt.Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
            c.setBackground(java.awt.Color.WHITE); // semua putih
            return c;
        }
    });
}

    
